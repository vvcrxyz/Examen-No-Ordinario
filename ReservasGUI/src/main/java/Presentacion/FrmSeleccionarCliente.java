    private void configurarTablaMesas() {
        
        modelTable = (DefaultTableModel) tblCliente.getModel();
        TableColumnModel modeloColumnas = tblCliente.getColumnModel();

        // Configurar renderizador y editor de bot√≥n
        modelTable.setRowCount(0);
        modeloColumnas.getColumn(2).setCellRenderer(new ButtonRenderer("Generar", new Color(221,188,0)));
        modeloColumnas.getColumn(2).setCellEditor(new ButtonEditor("Generar", new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                 
                    if(tblCliente.getSelectedRow() == -1)
                        return;
                    
                    try {
                        
                        FrmReporte frm = new FrmReporte(frmSeleccionarCliente, reservaNegocio.buscarReservasPorCliente(clientes.get(tblCliente.getSelectedRow())), clientes.get(tblCliente.getSelectedRow()));
                        frm.setVisible(true);
                        dispose();
                        
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(frmSeleccionarCliente, "Error en al buscar reportes.", "Error", JOptionPane.ERROR_MESSAGE);
                    }

                }
            }));

        
    } 
    
    private void mostrarDatosTabla() {
        DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();
        
        for (ClienteDTO cliente : clientes) {
            Object[] row = new Object[2];
            row[0] = cliente.getNombreCompleto();
            row[1] = cliente.getTelefono();
            
            model.addRow(row);
        }
    }
    
